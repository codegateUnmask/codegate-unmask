/**
 * 진단 결과(4축)로 학습 카테고리 우선순위를 정합니다.
 * 취약 축(authority/urgency/greed)은 점수가 높을수록 먼저,
 * verify는 낮을수록 먼저 배웁니다(습관이 없을수록 시급).
 */
export function recommendCategories(axes: VulnAxes | null | undefined): EduCategory[] {
  if (!axes) return EDU_CATEGORIES.map((c) => c.key);
  const urgencyOf = (axis: keyof VulnAxes) => (axis === 'verify' ? 100 - axes.verify : axes[axis]);
  return [...EDU_CATEGORIES]
    .sort((a, b) => urgencyOf(b.axis) - urgencyOf(a.axis))
    .map((c) => c.key);
}

export function lessonsOf(category: EduCategory | 'all'): EduLesson[] {
  return category === 'all' ? EDU_LESSONS : EDU_LESSONS.filter((l) => l.category === category);
}
