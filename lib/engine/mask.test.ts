import assert from 'node:assert/strict';

import { maskPII } from './mask';

const source = [
  '연락처 010-1234-5678',
  '주민번호 900101-1234567',
  '계좌 123-456789-012',
  '이메일 user@example.com',
].join('\n');

const expected = [
  '연락처 [연락처]',
  '주민번호 [주민번호]',
  '계좌 [계좌번호]',
  '이메일 [이메일]',
].join('\n');

assert.equal(maskPII(source), expected);
assert.equal(maskPII(source), expected, '반복 호출에서도 모든 전역 정규식이 동작해야 합니다.');
assert.equal(maskPII('민감정보가 없는 계약 조항입니다.'), '민감정보가 없는 계약 조항입니다.');
assert.equal(maskPII('01012345678 / 010 9876 5432'), '[연락처] / [연락처]');

// ★회귀 방지: 계좌번호 규칙이 날짜를 삼키면 안 됩니다.
//   과거 `2024-10-31`이 [계좌번호]로 마스킹돼 계약기간이 AI에 닿기 전에 사라졌습니다.
//   판독에 필요한 정보를 가리는 것은 과잉 마스킹이며 판정 품질을 직접 훼손합니다.
assert.equal(
  maskPII('계약기간 2024-10-31 부터 2026-10-30 까지'),
  '계약기간 2024-10-31 부터 2026-10-30 까지',
  '날짜는 마스킹되면 안 됩니다',
);
assert.equal(
  maskPII('임대차 기간은 2024년 10월 31일부터 24개월로 한다.'),
  '임대차 기간은 2024년 10월 31일부터 24개월로 한다.',
  '한글 날짜 표기도 보존되어야 합니다',
);
assert.equal(
  maskPII('보증금 300,000,000원 월세 1,500,000원'),
  '보증금 300,000,000원 월세 1,500,000원',
  '금액은 마스킹되면 안 됩니다',
);

// 계좌번호는 계좌 문맥이 있을 때 가립니다.
assert.match(maskPII('입금계좌 110-123-456789'), /\[계좌번호\]/);
assert.match(maskPII('예금주 홍길동 계좌 352-0123-4567-89'), /\[계좌번호\]/);

// 날짜와 계좌가 같이 있어도 각각 올바르게 처리되어야 합니다.
{
  const mixed = maskPII('계약일 2024-10-31, 입금계좌 110-123-456789 입니다.');
  assert.ok(mixed.includes('2024-10-31'), '날짜 보존 실패');
  assert.ok(mixed.includes('[계좌번호]'), '계좌 마스킹 실패');
}

// ── 이름 마스킹 (CLAUDE.md §8 "이름·연락처·계좌·주소를 가린다") ──
// 역할 라벨은 남기고 이름만 가립니다 — 라벨이 없으면 누구에 대한 조항인지 알 수 없습니다.
assert.equal(maskPII('임대인 김철수'), '임대인 [이름]');
assert.equal(maskPII('임차인: 홍길동'), '임차인: [이름]');
assert.equal(maskPII('근로자 성명 박영희'), '근로자 성명 [이름]');
assert.equal(maskPII('대표자 이순신 (인)'), '대표자 [이름] (인)');

// ★과잉 마스킹 방지: 계약 용어·일반 문장은 건드리면 안 됩니다.
assert.equal(
  maskPII('임대인은 목적물을 사용에 필요한 상태로 유지할 의무가 있다.'),
  '임대인은 목적물을 사용에 필요한 상태로 유지할 의무가 있다.',
  '조사가 붙은 역할어까지 이름으로 오인하면 안 됩니다',
);
assert.equal(
  maskPII('임차인이 원상복구 비용을 전액 부담한다.'),
  '임차인이 원상복구 비용을 전액 부담한다.',
);
// ★회귀 방지: 역할 라벨 뒤에 오는 '계약서 상용어'를 이름으로 오인하면 안 됩니다.
//   (실제로 '임대인 연락처는…'의 '연락처는'이 [이름]으로 바뀌어 문장이 망가졌었습니다)
assert.equal(
  maskPII('임대인 연락처는 010-1234-5678입니다.'),
  '임대인 연락처는 [연락처]입니다.',
);
assert.equal(maskPII('임차인 주소는 아래와 같다.'), '임차인 주소는 아래와 같다.');
assert.equal(maskPII('근로자 급여는 매월 지급한다.'), '근로자 급여는 매월 지급한다.');

// ── 주소 마스킹 ──
// 시/구까지는 남기고(시세·권리관계 맥락) 상세주소만 가립니다.
{
  const masked = maskPII('소재지 : 서울특별시 강남구 테헤란로 123, 45층 4501호');
  assert.ok(masked.includes('서울특별시 강남구'), '지역 정보는 보존되어야 합니다');
  assert.ok(masked.includes('[상세주소]'), '상세주소는 가려져야 합니다');
  assert.ok(!masked.includes('테헤란로 123'), '번지가 남으면 안 됩니다');
}

// 종합 — 실제 계약서 형태에서 필요한 정보는 살고 개인정보만 사라지는지
{
  const doc = maskPII(
    [
      '임대인 김철수 (연락처 010-1234-5678)',
      '임차인 홍길동 900101-1234567',
      '소재지 : 서울특별시 강남구 테헤란로 123, 45층 4501호',
      '임대차 기간: 2024-10-31 부터 2026-10-30 까지',
      '보증금 300,000,000원 / 월세 1,500,000원',
      '입금계좌 110-123-456789',
    ].join('\n'),
  );
  // 개인정보는 전부 사라져야 함
  for (const secret of ['김철수', '홍길동', '010-1234-5678', '900101-1234567', '테헤란로 123']) {
    assert.ok(!doc.includes(secret), `개인정보가 남았습니다: ${secret}`);
  }
  // 판독에 필요한 정보는 남아야 함
  for (const keep of ['2024-10-31', '2026-10-30', '300,000,000원', '1,500,000원', '강남구']) {
    assert.ok(doc.includes(keep), `판독에 필요한 정보가 사라졌습니다: ${keep}`);
  }
}

// ── 실제 문서로 재확인하다 발견한 유출들 (회귀 방지) ──────────────
// 위 종합 테스트는 통과하는데도 아래 형태가 그대로 새고 있었습니다.
// 단위 테스트가 통과한다고 안전한 게 아니라는 증거라, 사례를 그대로 박아둡니다.
{
  // ① 은행명이 앞에 오는 계좌 — 문맥 단어가 '계좌/입금'이 아니라 은행명이었습니다
  const a = maskPII('보증금은 국민은행 123-456-789012 (예금주 김철수) 로 입금한다.');
  assert.ok(!a.includes('123-456-789012'), '은행명 뒤 계좌번호가 남았습니다');
  assert.ok(!a.includes('김철수'), "'예금주 이름'이 남았습니다");

  // ② 앞자리가 4자리인 계좌 (우리은행 1002-…) — \d{2,3} 이라 안 걸렸습니다
  const b = maskPII('반송비를 우리은행 1002-345-678901 로 입금바랍니다.');
  assert.ok(!b.includes('1002-345-678901'), '앞자리 4자리 계좌가 남았습니다');

  // ③ '성함:' 라벨 — 스미싱 문자에서 흔한데 라벨 목록에 없었습니다
  const c = maskPII('성함: 이민수 / 연락처 010-2222-3333');
  assert.ok(!c.includes('이민수'), "'성함' 뒤 이름이 남았습니다");

  // ④ 계좌 패턴을 넓히면서 금액을 삼키지 않는지 — 구분자 없는 8자리는 계좌가 아닙니다
  const d = maskPII('입금 금액은 50000000원입니다.');
  assert.ok(d.includes('50000000'), '금액이 계좌번호로 오인되어 사라졌습니다');
}

// ── 3차 적대적 리체크에서 발견한 유출들 (회귀 방지) ──────────────
// 2차까지 통과한 상태에서도 아래 형태들이 그대로 샜습니다.
{
  // 전화번호 변형 — 점 구분·국제표기
  const a = maskPII('연락처: 010.1234.5678 / +82-10-2222-3333');
  assert.ok(!a.includes('010.1234.5678'), '점 구분 전화번호가 남았습니다');
  assert.ok(!a.includes('2222-3333'), '+82 국제표기 전화번호가 남았습니다');
  // 대표번호(1588)·날짜는 개인정보가 아니므로 보존
  const a2 = maskPII('고객센터 1588-1234 / 기간 2024-10-31');
  assert.ok(a2.includes('1588-1234'), '대표번호가 오인 마스킹됐습니다');
  assert.ok(a2.includes('2024-10-31'), '날짜가 오인 마스킹됐습니다');

  // 외국인등록번호(뒷자리 5~8) + 대시 양옆 공백
  const b = maskPII('외국인등록번호 950505-5678901 / 주민번호 880808 - 2345678');
  assert.ok(!b.includes('950505-5678901'), '외국인등록번호가 남았습니다');
  assert.ok(!b.includes('2345678'), '공백 낀 주민번호가 남았습니다');

  // 지번 주소 (도로명이 아닌 동+번지)
  const c = maskPII('소재지: 서울특별시 관악구 봉천동 123-45 302호');
  assert.ok(!c.includes('봉천동 123-45'), '지번 주소가 남았습니다');
  assert.ok(c.includes('관악구'), '지역(구 단위)은 보존되어야 합니다');

  // 카드번호 4-4-4-4
  const d = maskPII('결제카드: 1234-5678-9012-3456');
  assert.ok(!d.includes('1234-5678-9012-3456'), '카드번호가 남았습니다');

  // 택배 스미싱 라벨 (받는분/보내는분)
  const e = maskPII('받는분: 김민수\n보내는분: 박지영');
  assert.ok(!e.includes('김민수') && !e.includes('박지영'), '택배 라벨 뒤 이름이 남았습니다');

  // 사업자등록번호(공개정보)는 보존 — 사업자 진위 조회에 필요
  const f = maskPII('사업자등록번호: 123-45-67890');
  assert.ok(f.includes('123-45-67890'), '사업자등록번호가 오인 마스킹됐습니다');
}

