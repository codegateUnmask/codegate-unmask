export const MASK_PATTERNS = {
  phone: /01[016-9][-\s]?\d{3,4}[-\s]?\d{4}/g,
  rrn: /\d{6}[-\s]?[1-4]\d{6}/g,
  // ⚠️ 계좌번호는 "숫자-숫자-숫자" 모양만으로 잡으면 날짜(2024-10-31)까지 삼킵니다.
  //    실제로 계약기간이 [계좌번호]로 마스킹돼 AI가 기간 조항을 볼 수 없었습니다.
  //    그래서 ① 날짜로 보이는 것은 먼저 제외하고 ② 계좌 문맥 단어가 앞에 있을 때만 가립니다.
  //    ③ 실제 문서로 재확인한 결과 두 가지가 더 새고 있었습니다.
  //       · "국민은행 123-456-789012" — 은행명이 문맥 단어에 없었습니다.
  //       · "1002-345-678901"(우리은행) — 앞자리가 4자리라 \d{2,3}에 안 걸렸습니다.
  //       그래서 은행명을 문맥에 넣고 자릿수를 넓혔습니다.
  //    ④ 다만 구분자를 필수로 둡니다. 안 그러면 "50000000"(금액)까지 삼킵니다.
  //       구분자 없이 이어진 10자리 이상만 예외로 계좌로 봅니다.
  account: new RegExp(
    '(?<=(?:계좌|예금주|입금|송금|이체|은행|뱅크|농협|수협|신협|우체국|새마을금고)[^\\n]{0,20})' +
      '(?:\\d{2,6}[-\\s]\\d{2,6}(?:[-\\s]\\d{2,7})?|\\d{10,16})',
    'g',
  ),
  email: /[\w.+-]+@[\w-]+\.[\w.]+/g,
  // 이름 — 아무 두세 글자나 지우면 계약 문장이 망가지므로,
  // '임대인 홍길동'처럼 **역할 라벨 뒤에 오는 사람 이름**만 가립니다.
  // (라벨 자체는 남겨야 누구에 대한 조항인지 판독이 가능합니다)
  // 역할 라벨 뒤의 사람 이름만 가립니다.
  // 제외 목록이 핵심입니다 — '임대인 연락처는…'의 '연락처는'을 이름으로 오인하면
  // 문장이 망가져서 판독 자체가 나빠집니다(오탐이 미탐보다 해롭습니다).
  // 뒤에 조사가 붙어도 걸러지도록 제외어 뒤 조사를 함께 허용합니다.
  // ⚠️ 실제 문서로 재확인하며 '성함: 이민수', '(예금주 김철수)'가 그대로 새는 것을 발견해
  //    역할 라벨 목록을 넓혔습니다. 라벨이 없는 이름은 여전히 가리지 않습니다
  //    (아무 두세 글자나 지우면 조항 문장이 망가져 판독이 나빠집니다).
  name: new RegExp(
    '(?<=(?:임대인|임차인|매도인|매수인|근로자|사용자|사업주|대표자|성명|성함|이름|회원|고객|' +
      '예금주|수취인|계약자|신청인|가입자)\\s*[:：]?\\s*)' +
      '(?!(?:임대인|임차인|매도인|매수인|근로자|사용자|사업주|대표자|성명|성함|이름|회원|고객|' +
      '예금주|수취인|계약자|신청인|가입자|' +
      '연락처|전화|번호|주소|소재지|계좌|정보|서명|날인|의무|책임|동의|확인|본인|상호|법인|' +
      '주민|생년|직위|부서|사업|소득|급여|임금|보증|목적|사항|기간|조건)' +
      // ⚠️ \b는 한글에서 신뢰할 수 없습니다(한글은 JS 정규식의 \w가 아님).
      //    그래서 제외어 뒤 경계를 조사·공백·구두점으로 직접 씁니다.
      '(?:은|는|이|가|을|를|의|에|와|과|도|만|께|에게|으로|로)?(?:\\s|,|:|：|$))' +
      '[가-힣]{2,4}(?=\\s|,|\\(|\\)|$)',
    'gm',
  ),
  // 주소 — 시/도·구/군까지는 남기고 그 뒤 상세주소(번지·동·호)만 가립니다.
  // 지역은 시세·권리관계 판단 맥락이라 통째로 지우면 판독이 나빠집니다.
  addressDetail:
    /(?<=(?:특별시|광역시|특별자치시|특별자치도|[가-힣]{2,10}(?:시|군|구))\s)[가-힣0-9]+(?:로|길)\s*[\d-]+(?:[^\n,]{0,20}?(?:동|층|호))?/g,
} as const;

