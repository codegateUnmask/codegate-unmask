/**
 * 텍스트에 등장하는 글로서리 용어를 찾아 반환합니다. (LLM 호출 없음, 동기)
 * 등장 위치 순으로 정렬하고, 같은 용어는 한 번만 반환합니다.
 */
export function findGlossaryTerms(text: string): GlossaryEntry[] {
  return GLOSSARY
    .map((entry) => {
      const keys = [entry.term, ...(entry.aliases ?? [])];
      const indexes = keys.map((k) => text.indexOf(k)).filter((i) => i >= 0);
      return indexes.length ? { entry, at: Math.min(...indexes) } : null;
    })
    .filter((x): x is { entry: GlossaryEntry; at: number } => x !== null)
    .sort((a, b) => a.at - b.at)
    .map((x) => x.entry);
}
